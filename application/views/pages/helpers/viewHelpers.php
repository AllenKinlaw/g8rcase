function getLetterBackground($letter)
{
    switch ($letter){
        case 'A':
            return '000000' ;
        case 'B':
            return 'C0C0C0'  ;
        case 'C':
            return '808080' ;
        case 'D':
            return 'FF0000' ;
        case 'E':
            return 'F0A804' ;
         case 'F':
            return 'FFFF00' ;
         case 'G':
            return '008000' ;
         case 'H':
            return '0000FF' ;
          case 'I':
            return '800080' ;
         case 'J':
            return '000000' ;
        case 'K':
            return 'C0C0C0'  ;
        case 'L':
            return '808080' ;
        case 'M':
            return 'FF0000' ;
        case 'N':
            return 'F0A804' ;
         case 'O':
            return 'FFFF00' ;
         case 'P':
            return '008000' ;
         case 'Q':
            return '0000FF' ;
          case 'R':
            return '800080' ;
          case 'S':
            return '000000' ;
        case 'T':
            return 'C0C0C0'  ;
        case 'U':
            return '808080' ;
        case 'V':
            return 'FF0000' ;
        case 'W':
            return 'F0A804' ;
         case 'X':
            return 'FFFF00' ;
         case 'Y':
            return '008000' ;
         case 'Z':
            return '0000FF' ;
            
    }
    
}